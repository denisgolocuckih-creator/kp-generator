import os
from dotenv import load_dotenv
from langchain_openai import ChatOpenAI

# Загружаем ключ
load_dotenv()

# Шаг 1: Читаем прайс-лист вручную (без chromadb)
print("🔨 Загружаю базу знаний...")
with open("data/prices.txt", "r", encoding="utf-8") as f:
    price_list = f.read()

# Шаг 2: Подключаем DeepSeek через VseGPT
llm = ChatOpenAI(
    model="deepseek/deepseek-chat",
    temperature=0.1,
    openai_api_key=os.getenv("VSEGPT_API_KEY"),
    openai_api_base="https://api.vsegpt.ru/v1"
)

# Шаг 3: Системный промпт
SYSTEM_PROMPT = f"""
Ты — ИИ-калькулятор строительной компании «СтройИнвест» (г. Курск). 
Твоя задача: рассчитать коммерческое предложение СТРОГО на основе базы знаний.

Вот единственный источник цен, который ты можешь использовать:
=== НАЧАЛО БАЗЫ ЗНАНИЙ ===
{price_list}
=== КОНЕЦ БАЗЫ ЗНАНИЙ ===

ПРАВИЛА РАБОТЫ:
1. Получи описание объекта от пользователя.
2. Выдели параметры: тип объекта, площадь, класс отделки, особые условия.
3. Найди базовую ставку в базе знаний.
4. Посчитай стоимость: Площадь * Ставка.
5. Примени коэффициенты удорожания и скидки из базы знаний.
6. Выведи результат в формате JSON со всеми цифрами.
7. Если данных не хватает — ЗАДАЙ ВОПРОС. НЕ ДОДУМЫВАЙ.

ФОРМАТ ОТВЕТА ВСЕГДА ЗАКАНЧИВАЙ JSON:
{{
  "тип_объекта": "...",
  "площадь_квм": ...,
  "базовая_ставка_за_квм": ...,
  "базовая_стоимость": ...,
  "коэффициенты": [...],
  "скидки": [...],
  "итоговая_стоимость": ...,
  "срок_месяцев": ...,
  "комментарий": "..."
}}
"""

def calculate_kp(user_input: str) -> str:
    full_prompt = f"{SYSTEM_PROMPT}\n\nЗАПРОС ПОЛЬЗОВАТЕЛЯ:\n{user_input}\n\nВыполни расчет строго по правилам."
    result = llm.invoke(full_prompt)
    return result.content

# Запуск
if __name__ == "__main__":
    print("=" * 60)
    print("🧠 ИИ-КАЛЬКУЛЯТОР КОММЕРЧЕСКИХ ПРЕДЛОЖЕНИЙ v0.4 (VseGPT)")
    print("=" * 60)
    
    test_request = "Заказчик просит построить офис в центре Курска. Площадь 500 кв.м., отделка бизнес-класса. Нужно быстро, сроки горят."
    
    print(f"\n📥 ЗАПРОС: {test_request}")
    print("\n⏳ СЧИТАЮ...\n")
    
    response = calculate_kp(test_request)
    print(f"📊 РАСЧЕТ:\n{response}")
    
    print("\n✅ Проверьте цифры.")